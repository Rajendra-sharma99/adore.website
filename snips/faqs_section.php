      <details open>
        <summary>
          What does Adore India do?
        </summary>
        <div>
          Adore India is a platform which provides opportunities to the youth to work for the betterment of society.
          It’s a voluntary pan-India network.
        </div>
      </details>
      <details>
        <summary>
          Where is Adore India’s administrative office?
        </summary>
        <div>
          The administrative office is in Kolkata. Our volunteers are in over 35 cities of India.
        </div>
      </details>
      <details>
        <summary>
          How to join Adore India?
        </summary>
        <div>
          To join our volunteering network, fill the inquiry form so that we can guide you step by step. The entire
          process of volunteer registration can take a fortnight or more which includes filling up a few forms and
          uploading some documents besides a few rounds of discussion.
        </div>
      </details>
      <details>
        <summary>
          What are the volunteers required to do?
        </summary>
        <div>
          The volunteers form teams and participate in activities which build up the youth. The activities are mostly
          conducted in schools and localities through interactive sessions with students. Approaching schools and
          getting a positive response is a gradual process and requires patience and sincerity.
        </div>
      </details>
      <details>
        <summary>
          What is the location of work?
        </summary>
        <div>
          Volunteers are required to work in their respective towns or cities where they are studying or working. Local
          schools, orphanages, or informal community schools have to be selected for volunteering.
        </div>
      </details>
      <details>
        <summary>
          Will the volunteers be working all by themselves?
        </summary>
        <div>
          Volunteers can conduct activities by coordinating with the other existing volunteers in their city/town or
          team up with like-minded friends who are willing to work along.
        </div>
      </details>
      <details>
        <summary>
          What are the timings and activities?
        </summary>
        <div>
          There are no fixed working hours. Volunteers have to coordinate the time with their own work and suitability
          of school children. A few hours per week is all that is needed. Volunteers are expected to conduct sessions
          based on activities mentioned here. To conduct any other activities, objective and session plan needs to be
          submitted and approval sought.
        </div>
      </details>
      <details>
        <summary>
          What are the volunteering benefits?
        </summary>
        <div>
          There are no monetary benefits. But they get immense happiness and satisfaction for being able to contribute
          to the nation and society. It is our responsibility towards the nation in its betterment and development.
          Personality development, confidence building, network growth, and communication skills are some of the direct
          benefits of volunteering with Adore India.
          Volunteers can also apply for a certificate of volunteering based on the activity uploaded by them. However, a
          minimum of 75 valid Sessions must be conducted and a new set of volunteers must be handed over the
          responsibility to ensure continuity of activity. A youth impact webinar and an impact report is also necessary
          before the system generates the certificate.
        </div>
      </details>
      <details>
        <summary>
          What is the duration of volunteering?
        </summary>
        <div>
          We encourage volunteers to work for as long as possible. Since it is a part-time volunteering work, you can
          always be associated with it forever as a responsible citizen of India. However, before quitting volunteering,
          it is important that you pass on the responsibility of your center/school to another volunteer to ensure
          continuity of activities.
        </div>
      </details>
      <details>
        <summary>
          Is there an opportunity for internship ?
        </summary>
        <div>
          Internship opportunities are available from time to time click here for Applying. A minimum commitment of 360
          hours of work (over a period of 2-6 months) is needed to complete an internship. A comprehensive report is
          also required to be submitted at the end of the internship. Internship opportunities are available all over
          the country.
        </div>
      </details>
